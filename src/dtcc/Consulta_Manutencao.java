    private void salvar() {
        try {
            entityManager.getTransaction().commit();
            entityManager.getTransaction().begin();
            editarCadastros();
        } catch (RollbackException rex) {
            rex.printStackTrace();
            entityManager.getTransaction().begin();
            List<dtcc.Manutencao> merged = new ArrayList<dtcc.Manutencao>(list.size());
            for (dtcc.Manutencao m : list) {
                merged.add(entityManager.merge(m));
                atualizar();
            }
        }
    }

    private void atualizar() {
        entityManager.getTransaction().rollback();
        entityManager.getTransaction().begin();
        java.util.Collection data = query.getResultList();
        for (Object entity : data) {
            entityManager.refresh(entity);
            btnGerar.setEnabled(false);
        }
        list.clear();
        list.addAll(data);
        listarCadastros();
    }

    private void calculaValor() {
        Double preco = Double.parseDouble(txtPreco.getText());
        Double desconto = Double.parseDouble(txtDesconto.getText());
        Double qtde = Double.parseDouble(txtQtde.getText());
        Double resultado = preco * qtde - desconto;
        String result = Double.toString(resultado);
        txtTotal.setText(result);
    }

