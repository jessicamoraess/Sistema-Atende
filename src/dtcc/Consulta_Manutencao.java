    public void listarCadastros() {
        String sql = "Select * from manutencao order by Cod_Manutencao Asc ";
        try (Connection con = conexao.getConnection()) {
            PreparedStatement pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            masterTable.setModel(DbUtils.resultSetToTableModel(rs));
            txtPesquisar.setText("");
        } catch (SQLException error) {
            JOptionPane.showMessageDialog(null, error);

        }
    }

    public void editarCadastros() {
        String sql = "Update manutencao set Cod_Cli =?, Nome_Cliente =? , Cod_Fun =? , Nome_Funcionario =?, Ultima_Manutencao =?, Proxima_Manutencao =?, Tipo_Manutencao =?, Descricao_Do_Produto =?, Descricao_Do_Problema =?  where Cod_Manutencao like ?";

        try (Connection con = conexao.getConnection()) {
            PreparedStatement pst = con.prepareStatement(sql);

            pst.setString(1, codCliField.getText());
            pst.setString(2, nomeClienteField.getText());
            pst.setString(3, codFunField.getText());
            pst.setString(4, nomeFuncionarioField.getText());
            pst.setString(5, ultimaManutencaoField.getText());
            pst.setString(6, proximaManutencaoField.getText());
            pst.setString(7, tipoManutencaoField.getText());
            pst.setString(8, descricaoDoProdutoField.getText());
            pst.setString(9, descricaoDoProblemaField.getText());
            pst.setString(10, codManutencaoField.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Cadastro atualizado com sucesso.");
            listarCadastros();

        } catch (SQLException error) {
            JOptionPane.showMessageDialog(null, error);

        }

    }

