    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        BD conn = new BD();
        try {
            conn.conectando();

            if (contador == 2) {

                new TratamentoDeEmail().enviarEmail();

            } else {

                contador++;

            }

            String sql = "select * from login_senha Where Login like '" + txtLogin.getText() + "' and Senha like  '" + txtSenha.getText() + "' ;";
            conn.executaSQL(sql);

            if (BD.rs.first()) {

                dispose();
                new FrontEnd().setVisible(true);
            } else {

                JOptionPane.showMessageDialog(null, "O nome de usuário ou senha foram digitados incorretamente", "Dados Incorretos", JOptionPane.WARNING_MESSAGE);
                System.out.println("Dados Invalidos");

            }

        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnEntrarActionPerformed

