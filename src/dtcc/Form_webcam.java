    private void btnCapturarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapturarActionPerformed
        FrameGrabbingControl fgc = (FrameGrabbingControl) player.getControl("javax.media.control.FrameGrabbingControl");
        Buffer buf = fgc.grabFrame();

        BufferToImage bi = new BufferToImage((VideoFormat) buf.getFormat());
        image = bi.createImage(buf);  // TODO add your handling code here:
        JOptionPane.showMessageDialog(this, "Imagem Capturada!");
    }//GEN-LAST:event_btnCapturarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        /**
           }//GEN-LAST:event_btnSalvarActionPerformed
*
         */

        JFileChooser fileChooser = new JFileChooser();
        int saveValue = fileChooser.showSaveDialog(null);
        if (saveValue == JFileChooser.APPROVE_OPTION) {
            try {
                ImageIO.write(toBufferedImage(image), "png", new File(fileChooser.getSelectedFile().getAbsolutePath() + ".png"));
            } catch (IOException e) {
                e.printStackTrace();
            }
            JOptionPane.showMessageDialog(this, "Imagem Salva!");
        }
    }

