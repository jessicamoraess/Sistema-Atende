    private void geraOrcamento() {
        try (Connection con = conexao.getConnection()) {
            String sql = "Select Nome_Cliente, Nome_Funcionario, Descricao_Produto from instalacao where Cod_Instalacao = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, txtCodInstalacao.getText() + "%");
            rs = pst.executeQuery();
            rs.first();
            txtCliente.setText(rs.getString("Nome_Cliente"));
            txtVendedor.setText(rs.getString("Nome_Funcionario"));
            txtDescricao.setText(rs.getString("Descricao_Produto"));

        } catch (SQLException error) {

        }
    }
