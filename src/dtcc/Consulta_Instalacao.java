    public void listarCadastros() {
        String sql = "Select * from instalacao order by Cod_Instalacao Asc ";
        try (Connection con = conexao.getConnection()) {
            PreparedStatement pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            masterTable.setModel(DbUtils.resultSetToTableModel(rs));
            txtPesquisar.setText("");
        } catch (SQLException error) {
            JOptionPane.showMessageDialog(null, error);

        }
    }

    public void editarCadastros() {
        String sql = "Update instalacao set  Cod_Cli  = ?, Nome_Cliente  = ?, Cod_Fun  = ?, Nome_Funcionario = ?, Primeira_Manutencao = ?, Descricao_Produto = ?, Data_Instalacao = ?, Horario = ? where Cod_Instalacao = ?";

        try (Connection con = conexao.getConnection()) {
            PreparedStatement pst = con.prepareStatement(sql);

            pst.setString(1, codCliField.getText());
            pst.setString(2, nomeClienteField.getText());
            pst.setString(3, codFunField.getText());
            pst.setString(4, nomeFuncionarioField.getText());
            pst.setString(5, primeiraManutencaoField.getText());
            pst.setString(6, descricaoProdutoField.getText());
            pst.setString(7, dataInstalacaoField.getText());
            pst.setString(8, horarioField.getText());
            pst.setString(9, codInstalacaoField.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Cadastro atualizado com sucesso.");
            listarCadastros();

        } catch (SQLException error) {
            JOptionPane.showMessageDialog(null, error);

        }

    }

